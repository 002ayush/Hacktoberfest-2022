//introduction to kotlin
fun main(args: Array<String>) {
  println("Hello World")
  val a="new hello from variable"
  println(a)
  val b=10
  println(b)
  val c : Int =20
  println(c)
  val d: String = "how are you ?"
  println(d)
  val e:Boolean = true
  println(e)
  val f:Float = 10.0F
  println(f)
  //we dont have to use 'F' in  double
  val g:Double = 10.999
  println(g)
  //variable
  var x : String = "hi"
  println(x)
  x="by"
  println(x)
  //val are fixed 
  //var are changable 
  // but var datatype cannot be changed
}
